## Data types in python ##
# Primitive
# str -> String
favorite_movie = "Avengers"
print(type(favorite_movie))

# int -> Integer
my_age = 34
print(type(my_age))

# float -> Float
my_height = 1.74
print(type(my_height))

# True -> boolean
married = True
print(type(married))

# False -> boolean
german = False
print(type(german))

## non primitive data types ##
# list -> Arrays
infiinity_stones = ["power", "time"]
print(type(infiinity_stones))

# tuple - cannot change a value defined
my_touple = (1, 2, 3)
print(type(my_touple))

# set -  avoid repeated values
my_set = {2, 3, 3, 4, 5, 6}
print(type(my_set))
print(my_set)

# None - To assign a none value
none_variable = None
print(type(none_variable))

# Dictionary - to set key-value pairs
my_dictionary = { 1: "el", 2: "la", 3: "los", 4: "las"}
print(type(my_dictionary))

